void main() {
  //This program demonstrates different data types in dart
  //variable declarations.
  //Number data type; for declaration of numeric values.
  num age = 20;
  //Integer data type. For declaration of integers.
  int num1 = 8;
  //String data type. Used to declare stringed data.
  String first_name = "Michael.";
  //double data type. used to declare numbers in double precision.
  double num2 = 20.54;
  //boolean data type. used to determine true or false.
  bool isMarried = false;
  // Outputs.
  print("Age is $age");
  print("Number is $num1");
  print("First name is $first_name");
  print("Double number is $num2");
  print("Marital status is $isMarried");
  //maps. A map describes paired collections having keys and values.
  var map = {'position1': 'student', 'position2': 'Engineer'};
  print(map);
}
